package hw6;

public class Calculator {

    static int addition(int num, int num2) {
        return num + num2;
    }

    static int subtraction(int num, int num2) {
        return num - num2;
    }

    static int multiplication(int num, int num2) {
        return num * num2;
    }

    static int division(int num, int num2) {
        return num / num2;
    }
}
